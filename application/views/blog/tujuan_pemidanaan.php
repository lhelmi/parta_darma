                <h2 class="entry-title">Tujuan Pemidanaan</h2>

<p>Pada umumnya tujuan hukum pidana untuk melindungi kepentingan individu atau melindungi hak asasi manusia dan melindungi kepentingan masyarakat maupun negara dari perbuatan kejahatan atau perbuatan tercela yang merugikan individu, masyarakat dan negara, dan juga menjaga agar penguasa tidak bertindak sewenang-wenang pada individu atau masyarakat.
<div class="container">Tujuan pemidanaan menurut Wirjono Prodjodikoro (1989 : 16), yaitu :
<br>
<br>a) Untuk menakut-nakuti orang jangan sampai melakukan kejahatan baik secara menakut-nakuti orang banyak (generals preventif) maupun menakut-nakuti orang tertentu yang sudah melakukan kejahatan agar dikemudian hari tidak melakukan kejahatan lagi (speciale preventif), atau

<br>b) Untuk mendidik atau memperbaiki orang-orang yang melakukan kejahatan agar menjadi orang-orang yang baik tabiatnya sehingga bermanfaat bagi masyarakat.
</div>
<p>Tujuan pemidanaan itu sendiri diharapkan dapat menjadi sarana

perlindungan masyarakat, rehabilitasi dan resosialisasi, pemenuhan pandangan

hukum adat, serta aspek psikologi untuk menghilangkan rasa bersalah bagi yang

bersangkutan. Meskipun pidana merupakan suatu nestapa tetapi tidak

dimaksudkan untuk menderitakan dan merendahkan martabat manusia.

</div>
