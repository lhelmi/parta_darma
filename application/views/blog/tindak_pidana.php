                <h2 class="entry-title">Pengertian Tindak Pidana</h2>
                <div class="container">
                <p>Perbuatan Pidana menurut Moeljatno (2008: 77) adalah:
<br>pengertian perbuatan pidana adalah perbuatan yang dilarang oleh suatu aturan hukum larangan yang mana disertai ancaman (sangsi) yang berupa pidana tertentu bagi barang siapa melanggar larangan tersebut.

<p>Perbuatan Pidana menurut Wirjono Prodjodikoro (2002: 14) menyebutkan:


<br>Hukum merupakan rangkaian peraturan-peraturan mengenai tingkah laku orang-orang sebagai anggota masyarakat, sedangkan satu-satunya tujuan dari hukum ialah mengadakan keselamatan, kebahagiaan dan tata tertib dalam masyarakat.
</div>
<p>Dapat juga dikatakan bahwa perbuatan pidana adalah perbuatan yang oleh

suatu aturan hukum dilarang dan diancam pidana, asal saja dalam pada itu diingat

bahwa larangan ditunjukkan kepada perbuataan, (yaitu suatu keadaan atau

kejadiaan yang ditimbulkan oleh kelakuan orang), sedangkan ancaman pidananya

ditunjukkan kepada orang yang menimbulkannya kejadian itu. Antara larangan

dan ancaman pidana ada hubungan yang erat, oleh karena antara kajadian dan orang yang menimbulkan kejadian itu, ada hubungan yang erat pula. Dan justru untuk menyatakan hubungan yang erat itu; maka dipakailah perkataan perbuatan, yaitu suatu pengertian abstrak yang menunjukkan kepada dua keadaan konkrit: pertama, adanya kejadian yang tertentu dan kedua, adanya orang yang berbuat, yang menimbulkan kejadian itu.
<p>Ada lain istilah yang dipakai dalam hukum pidana, yaitu “tindak pidana”. Istilah ini, karena timbulnya dari pihak kementrian kehakiman, sering dipakai dalam perundang-undanagan. Meskipun kata “tindak” lebih pendek dari ”perbuatan” tapi “tindak “ tidak menunjukkan pada suatu yang abstrak seperti perbuatan, tapi hanya menyatakan perbuatan konkrit, sebagaimana halnya dengan peristiwa dengan perbedaan bahwa tindak adalah kelakuan, tingkah laku, gerak-gerik atau sikap jasmani seseorang . Oleh karena tindak sebagai kata tidak begitu dikenal, maka dalam perundang-undangan yang menggunakan istilah tindak pidana baik dalam pasal-pasal sendiri, maupun dalam penjelasannya hampir selalu dipakai pula kata perbuatan.

</div>
