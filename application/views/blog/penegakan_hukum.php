                <h2 class="entry-title">Penegakan Hukum dalam perspektif Sistem Peradilan Pidana</h2>

                <p>Penegakan hukum adalah proses dilakukannya upaya untuk tegaknya atau berfungsinya norma-norma hukum secara nyata sebagai pedoman perilaku dalam lalu lintas atau hubungan-hubungan hukum dalam kehidupan bermasyarakat dan bernegara. Ditinjau dari sudut subjeknya, penegakan hukum itu dapat dilakukan oleh subjek yang luas dan dapat pula diartikan sebagai upaya penegakan hukum oleh subjek dalam arti yang terbatas atau sempit.
                <p>Dalam arti luas, proses penegakan hukum itu melibatkan semua subjek hukum dalam setiap hubungan hukum. Siapa saja yang menjalankan aturan normatif atau melakukan sesuatu atau tidak melakukan sesuatu dengan mendasarkan diri pada norma aturan hukum yang berlaku, berarti dia menjalankan atau menegakkan aturan hukum.
                <p>Dalam arti sempit, dari segi subjeknya itu, penegakan hukum itu hanya diartikan sebagai upaya aparatur penegakan hukum tertentu untuk menjamin dan memastikan bahwa suatu aturan hukum berjalan sebagaimana seharusnya. Dalam memastikan tegaknya hukum itu, apabila diperlukan, aparatur penegak hukum itu diperkenankan untuk menggunakan daya paksa.
                <p>Norma hukum yang hendak ditegakkan mencakup pengertian hukum formal dan hukum materiel. Hukum formal hanya bersangkutan dengan peraturan perundang-undangan yang tertulis, sedangkan hukum materiil mencakup pula pengertian nilai-nilai keadilan yang hidup dalam masyarakat.
                <p>Penegakan hukum juga tidak terlepas dari dari aparatur penegak hukum. Aparatur penegak hukum mencakup pengertian mengenai institusi penegak hukum dan aparat (orangnya) penegak hukum. Dalam arti sempit, aparatur penegak hukum yang terlibat dalam proses tegaknya hukum itu, dimulai dari saksi, polisi, penasehat hukum, jaksa, hakim, dan petugas sipir pemasyarakatan. Setiap aparat dan aparatur terkait mencakup pula pihak-pihak yang bersangkutan dengan tugas atau perannya yaitu terkait dengan kegiatan pelaporan atau pengaduan, penyelidikan, penyidikan, penuntutan, pembuktian, penjatuhan vonis dan pemberian sanksi, serta upaya pemasyarakatan kembali (resosialisasi) terpidana.
                <p>Dalam proses bekerjanya aparatur penegak hukum itu, terdapat tiga elemen penting yang mempengaruhi, yaitu: institusi penegak hukum beserta berbagai perangkat sarana dan prasarana pendukung dan mekanisme kerja

kelembagaannya,budaya kerja yang terkait dengan aparatnya, termasuk mengenai

kesejahteraan aparatnya, dan perangkat peraturan yang mendukung baik kinerja

kelembagaannya maupun yang mengatur materi hukum yang dijadikan standar

kerja, baik hukum materielnya maupun hukum acaranya. Upaya penegakan

hukum secara sistemik haruslah memperhatikan ketiga aspek itu secara simultan,

sehingga proses penegakan hukum dan keadilan itu sendiri secara internal dapat

diwujudkan secara nyata.

