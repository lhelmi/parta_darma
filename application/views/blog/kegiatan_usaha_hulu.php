                <h2 class="entry-title">Kegiatan Usaha Hulu</h2>

<p>Dalam dunia pertambangan Minyak dan Gas Bumi, ada dua kegiatan usaha, yaitu Kegiatan Usaha Hulu dan Kegiatan Usaha Hilir. menurut pasal 1 ayat 7 UU No. 22 tahun 2001 tentang Minyak dan Gas Bumi bahwa Kegiatan Usaha Hulu adalah
<br>“kegiatan usaha yang berintikan atau bertumpu pada kegiatan usaha Eksplorasi dan Eksploitasi.”
<p>Kegiatan Usaha Hulu sebagaimana dimaksud dalam Pasal 5 angka 1 dilaksanakan dan dikendalikan melalui Kontrak Kerja Sama, dan paling sedikit memuat persyaratan :
<br><br>a. kepemilikan sumber daya alam tetap di tangan Pemerintah sampai pada titik penyerahan;
<br>b. pengendalian manajemen operasi berada pada Badan Pelaksana;
<p>modal dan risiko seluruhnya ditanggung Badan Usaha atau Bentuk Usaha Tetap.
<p><b>Pengertian Eksplorasi Minyak dan Gas Bumi</b>

<p>Sebagaimana diketahui, minyak dan gas bumi merupakan sumber daya alam yang tidak dapat diperbaharui (nonrenewable). Tadinya Indonesia merupakan Negara pengekspor neto minyak maupun gas bumi. Kini Indonesia merupakan pengimpor neto minyak bumi karena produksi nasional sudah tidak lagi mencukupi kebutuhan nasional. Di lain pihak, Indonesia dewasa ini merupakan salah satu produsen dan eksportir gas alam terbesar di dunia.

<p>Hampir seluruh kegiatan produksi minyak dan gas bumi di Indonesia dilakukan oleh perusahaan asing. Karena terus menerus dilanda KKN, peranan Pertamina dalam ekplorasi dan penambangan migas jauh tertinggal dari perusahaan yang lebih muda seperti Petronas dan Citic. Karena pemberian hak monopoli, peranan Pertamina yang menonjol hanya pada pengilangan dan distribusi di dalam negeri. Karena keterbatasan modal, keahlian dan pengalaman, keikut sertaan perusahaan swasta nasional dalam eksplorasi dan penambangan minyak baru pada tahap awal. Kasus semburan lumpur di Sidoarjo menggambarkan keterbatasan perusahaan swasta nasional dalam ekplorasi dan eksploitasi migas.
<p>Kegiatan eksplorasi sumber daya mineral, batubara dan sumber daya geologi lainnya sangat tergantung pada permintaan, harga, ongkos produksi, teknologi pengolahan dan kondisi politik, ekonomi dan hukum serta keamanan suatu Negara. Oleh karenanya diperlukan kajian pemilihan mineral untuk kepentingan eksplorasi sumberdaya mineral, batubara dan sumber daya bumi lainnya.
<p>Indonesia yang secara geologi, sangat menarik untuk terbentuknya mineral, batubara, panas bumi dan minyak dan gas bumi. Berdasarkan hasil kajian besaran nilai pasar, kondisi geologi dan minat investasi di Indonesia, diusulkan jenis mineral yang diutamakan untuk dieksplorasi yang terdiri dari emas, tembaga dan mineral serta jenis minyak dan gas bumi.
<p>Dalam kamus besar bahasa Indonesia, eksplorasi adalah Penjelajahan lapangan dengan tujuan memperoleh pengetahuan lebih banyak tentang keadaan, terutama sumber-sumber alam yang terdapat di tempat itu; penyelidikan; penjajakan.
<p>Menurut situs Wikipedia berbahasa Indonesia (id.wikipedia.org) eksplorasi diartikan tindakan mencari atau melakukan perjalanan dengan tujuan menemukan sesuatu; misalnya daerah tak dikenal, termasuk antariksa (penjelajahan angkasa), minyak bumi (eksplorasi minyak bumi), gas alam, batubara, mineral, gua, air, ataupun informasi.
<p>Menurut Standar Nasional Indonesia (SNI) Eksplorasi adalah kegiatan penyelidikan geologi yang dilakukan untuk mengidentifikasi,menetukan lokasi, ukuran, bentuk, letak, sebaran, kuantitas dan kualitas suatu endapan bahan galian untuk kemudian dapat dilakukan analisis/kajian kemungkinan dilakukanya penambangan.
<p>Adapun pengertian dari Eksplorasi sendiri diatur di dalam pasal 1 ayat 8 UU No. 22 tahun 2001 tentang Minyak dan Gas Bumi bahwa Eksplorasi adalah:
<div class="container">
“Eksplorasi adalah kegiatan yang bertujuan memperoleh informasi mengenai kondisi geologi untuk menemukan dan memperoleh perkiraan cadangan Minyak dan Gas Bumi di Wilayah Kerja yang ditentukan;”
</div>
<p>Dari ke-empat pengertian tentang eksplorasi diatas serta obyek yang diteliti dalam hal ini minyak dan gas bumi, dapat disimpulkan bahwa Eksplorasi adalah suatu kegiatan lanjutan dari prospeksi yang meliputi pekerjaan-pekerjaan untuk mengetahui ukuran,bentuk, posisi, kadar rata-rata dan esarnya cadangan serta “studi kalayakan” dari minyak dan gas bumi yang telah diketemukan.

<p>Sedangkan Studi Kelayakan adalah pengkajian mengenai aspek teknik dan prospek ekonomis dari suatu proyek penambangan dan merupakan dasar keputusan investasi. Kajian ini merupakan dokumen yang memenuhi syarat dan dapat diterima untuk keperluan analisa bank/lembaga keungan lainnya dalam kaitannya dengan pelaksanaan investasi atau pembiayaan proyek. Studi ini meliputi Pemeriksaan seluruh informasi geologi berdasarkan laporan eksplorasi dan factor-faktor ekonomi, penambangan, pengolahan, pemasaran hukum/perundang-undangan, lingkungan, sosial serta faktor yang terkait.

<p><b>Pengertian eksploitasi minyak dan gas bumi</b>

<p>Menurut situs Wikipedia berbahasa Indonesia (id.wikipedia.org) eksploitasi adalah politik pemanfaatan yang secara sewenang-wenang terlalu berlebihan terhadap sesuatu subyek eksploitasi hanya untuk kepentingan ekonomi semata-mata tanpa mempertimbangan rasa kepatutan, keadilan serta kompensasi kesejahteraan.

Menurut kamus hukum eksploitasi yaitu tindakan dengan atau tanpa

persetujuan korban yang meliputi tetapi tidak terbatas pada pelacuran, kerja atau

pelayanan paksa, perbudakan atau praktik serupa perbudakan, penindasan,

pemerasan, pemanfaatan fisik, seksual, organ reproduksi, atau secara melawan

hukum memindahkan atau mentransplantasi organ dan/atau jaringan tubuh atau
memanfaatkan tenaga atau kemampuan seseorang oleh pihak lain untuk
mendapatkan keuntungan baik materiil maupun immateriil.

<p>Sedangkan menurut kamus besar bahasa indonesia, eksploitasi adalah

pengusahaan atau pendayagunaan nikel di daerah itu dilakukan oleh perusahaan

asing atau pemanfaatan untuk keuntungan sendiri, pengisapan.

<p>Namun semua pengertian diatas rancu jika dihubungkan dengan minyak

dan gas bumi, pengertian dari Eksplorasi sendiri diatur di dalam pasal 1 angka 9

UU No. 22 tahun 2001 tentang Minyak dan Gas Bumi bahwa Eksploitasi adalah<div class="container">

“rangkaian kegiatan yang bertujuan untuk menghasilkan Minyak dan Gas Bumi dari Wilayah Kerja yang ditentukan, yang terdiri atas pengeboran dan penyelesaian sumur, pembangunan sarana pengangkutan, penyimpanan, dan pengolahan untuk pemisahan dan pemurnian Minyak dan Gas Bumi di lapangan serta kegiatan lain yang mendukungnya”
</div>
<p>Jadi eksploitasi intinya tindakan berkelanjutan setelah dilakuakan

eksplorasi dan hasil dari eksplorasi itu menunjukan adanya sumber minyak dan gas bumi.

<p>Kegiatan Usaha Hulu yang meliputi eksplorasi dan eksploitasi ini hanya dapat dilakukan oleh badan usaha yang memiliki bentuk usaha tetap misalnya bandan usaha milik negara/ badan usaha milik daerah yang kontrak kerjasamanya diberitahukan kepada Dewan Perwakilan Rakyat Republik Indonesia. Wilayah kerja sendiri ditetapkan oleh menteri setelah berkonsultasi dengan Pemerintah Daerah.

