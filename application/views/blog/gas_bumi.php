                <h2 class="entry-title">Gas Bumi</h2>

<p>Dalam kamus besar bahasa Indonesia, Gas bumi sering juga disebut sebagai gas alam atau gas rawa, adalah bahan bakar fosilberbentuk gas yang terutama terdiri dari metana CH4). Ia dapat ditemukan di ladang minyak, ladang gas bumi dan juga tambang batu bara.
<p>Adapun pengertian dari Gas Bumi sendiri diatur di dalam Pasal 1 ayat (2) UU No. 22 tahun 2001 tentang Minyak dan Gas Bumi bahwa Gas Bumi adalah
<br>“hasil proses alami berupa hidrokarbon yang dalam kondisi tekanan dan temperatur atmosfer berupa fasa gas yang diperoleh dari proses penambangan Minyak dan Gas Bumi.”
<p>Dari penjelasan pasal di atas dapat diketahui bahwa Gas Bumi adalah hasil proses alami berupa hidrokarbon yang dalam kondisi tekanan dan temperatur atmosfer berupa fasa gas yang diperoleh dari proses penambangan Minyak dan Gas Bumi. Sedangkan penambangan minyak dan gas bumi sendiri ada dua tahap yaitu proses eksplorasi dan eksploitasi (kegiatan hulu) disamping kegiatan hilir lainnya ( seperti Pengolahan, Pengangkutan, Penyimpanan, dan/atau Niaga).

