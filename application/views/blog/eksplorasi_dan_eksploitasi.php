                <h2 class="entry-title">Eksplorasi Dan Eksploitasi Dalam Perundang-Undangan Indonesia</h2>

<p>Pengusahaan pertambangan migas di Indonesia mengalami banyak perubahan. Namun yang paling banyak perubahannya adalah dari sektor kuasa pertambangan.
<p>Kuasa Pertambangan adalah wewnang yang diberikan kepada perusahaan negara untuk melaksanakan usaha pertambangan minyak dan gas bumi. (Adrian Sutedi, 2011: 54). Kuasa pertambangan negara diberikan kepada perusahaan negara atas dasar pemikiran bahwa tidak dapat diberikan hak yang lebih dari hak menguasai, dimana hak menguasai atas minyak dan gas bumi sebagai bahan galian khususyang penting bagi hajat hidup orang banyak hanya pada Negara Republik Indonesia.
<p>Berdasarkan ketentuan Pasal 6 UU no 44 Prp tahun 1960, Menteri Keuangan dapat menunjuk kontraktor untuk perusahaan negara guna melaksanakan pekerjaan yang belum atau tidak dapt dilaksanakan oleh perusahaan negara. Hal ini ini menjadikan perusahaan asing untuk bekerja sebagai kontraktor perusahaan Negara. Jadi perusahaan asing bisa melakukan kegiatan eksplorasi dan eksploitasi sebagai kontraktor perusahaan negara.
<p>Selanjutkan pada tahun 1971 saat diundangkannya UU no 8 tahun 1971 tentang Perusahaan Pertambangan Minyak dan Gas Bumi sebagai satu-satunya perusahaan negara pemegang Kuasa Pertambangan di Indonesia. Hal ini menjadikan Pertamina dapat mengadakan kerjasama dengan pihak lain termasuk para penambang tradisional.
<p>Pada perkembangan selanjutnya di tahun 2001 diundangkannya UU no 22 tahun 2001 tentang minyak dan gas bumi. Dalam UU ini yang banyak perubahan yang terjadi dalam dunia minyak dan gas bumi salah satunya adalah kuasa pertambangan dikuasai oleh pemerintah dan hanya meliputi kegiatan eksplorasi dan eksploitasi. Hal ini menjadikan Pertamina sekrang hanyak menjadi perusahaan biasa yang harus bersaing dengan perusahaan asing. Dampaknya penambang tradisional yang dahulu berkerjasama dengan pertamina sekarang menjadi penambang lepas karena perubahan regulasi ini.

