                <h2 class="entry-title">Subyek Tindak Pidana</h2>

<p>Sebagaimana diuraikan terdahulu, bahwa unsur pertama tindak pidana itu

adalah perbuatan orang, pada dasarnya yang dapat melakukan tindak pidana itu

manusia (naturlijke personen). Ini dapat disimpulkan berdasarkan hal-hal sebagai

berikut :
<div class="container">
    <p>a. Rumusan delik dalam undang-undang lazim dimulai dengan kata-kata : “barang siapa yang …….”. Kata “barang siapa” ini tidak dapat diartikan lain dari pada “orang”.
    <p>b. Dalam pasal 10 KUHP disebutkan jenis-jenis pidana yang dapat dikenakan kepada tindak pidana, yaitu :
    <div class="container">
        1) pidana pokok :
        
        <div class="container">
            <br>a) pidana mati
            
            <br>b) pidana penjara
            
            <br>c) pidana kurungan
            
            <br>d) pidana denda, yang dapat diganti dengan pidana kurungan
        </div>
            
            <br><br>2) pidana tambahan :
                <div class="container">

                    <br>a) pencabutan hak-hak tertentu
                    
                    <br>b) perampasan barang-barang tertentu
                    
                    <br>c) dimumkannya keputusan hakim
                </div>

</div>
<p>Sifat dari pidana tersebut adalah sedemikian rupa, sehingga pada dasarnya hanya dapat dikenakan pada manusia.

<p>c. Dalam pemeriksaan perkara dan juga sifat dari hukum pidana yang dilihat ada / tidaknya kesalahan pada terdakwa, memberi petunjuk bahwa yang dapat dipertanggungjawabkan itu adalah manusia.
<p>d. Pengertian kesalahan yang dapat berupa kesengajaan dan kealpaan itu

merupakan sikap dalam batin manusia.
</div>
<p>Dalam perkembangannya tindak pidana tidak hanya dilakukan manusia, dalam KUHP terdapat pasal yang seakan-akan menyinggung soal ini, ialah pasal
59. Pasal ini tidak menunjuk ke arah dapat dipidana suatu badan hukum, suatu perkumpulan atau badan (korporasi) lain. Menurut pasal ini yang dapat dipidana adalah orang yang melakukan sesuatu fungsi dalam sesuatu korporasi. Seorang anggota pengurus dapat membebaskan diri, apabila dapat membuktikan bahwa pelanggaran itu dilakukan tanpa ikut campurnya.

