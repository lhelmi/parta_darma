                <h2 class="entry-title">Minyak Bumi</h2>

                <p>Menurut pasal 1 ayat 1 Undang- Undang No 22 tahun 2001 tentang Minyak dan Gas Bumi, Minyak Bumi adalah hasil proses alami berupa hidrokarbon yang dalam kondisi tekanan dan temperatur atmosfer berupa fasa cair atau padat, termasuk aspal, lilin mineral atau ozokerit, dan bitumen yang diperoleh dari proses penambangan, tetapi tidak termasuk batubara atau endapan hidrokarbon lain yang berbentuk padat yang diperoleh dari kegiatan yang tidak berkaitan dengan kegiatan usaha Minyak dan Gas Bumi.
                <p>Dari penjelasan pasal di atas dapat diketahui bahwa minyak bumi dapat diperoleh dari proses pertambangan selain batubara dan endapan hidrokarbon lain yang berbentuk padat.
                <p>Sedangkan dalam kaidah bahasa Minyak bumi (bahasa Inggris: petroleum, dari bahasa Latin petrus – karang danoleum – minyak), dijuluki juga sebagai emas hitam, adalah cairan kental, coklat gelap, atau kehijauan yang mudah terbakar, yang berada di lapisan atas dari beberapa area di kerak Bumi. Minyak bumi terdiri dari campuran kompleks dari berbagai hidrokarbon, sebagian besar seri alkana, tetapi bervariasi dalam penampilan, komposisi, dan kemurniannya.

