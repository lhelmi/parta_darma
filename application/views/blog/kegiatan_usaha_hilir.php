                <h2 class="entry-title">Kegiatan Usaha Hilir</h2>

<p>Kegiatan Usaha Hilir dilaksanakan oleh Badan Usaha yang telah memiliki Izin Usaha yang dikeluarkan oleh Menteri ESDM. Direktorat Jenderal Minyak dan Gas Bumi.
<p>Izin Usaha Kegiatan Usaha Hilir dibedakan menjadi:
<br><br>
a. Izin Usaha Pengolahan;
<br>
b. Izin Usaha Pengangkutan;
<br>
c. Izin Usaha Penyimpanan;
<br>
d. Izin Usaha Niaga.
<br>
<p>Pengertian pengolahan menurut Undang-undang Republik Indonesia Nomor 22 Tahun 2001 Tentang Minyak dan Gas Bumi pasal 1 angka 11 yaitu :
<br>pengolahan adalah kegiatan memurnikan, memperoleh bagian-bagian, mempertinggi mutu, dan mempertinggi nilai tambah minyak bumi dan/atau gas bumi, tetapi tidak termasuk pengolahan lapangan

<p>Pengertian pengangkutan menurut Undang-undang Republik Indonesia Nomor 22 Tahun 2001 Tentang Minyak dan Gas Bumi pasal 1 angka 12 adalah “kegiatan pemindahan Minyak Bumi, Gas Bumi, dan/atau hasil olahannya dari Wilayah Kerja atau dari tempat penampungan dan Pengolahan, termasuk pengangkutan Gas Bumi melalui pipa transmisi dan distribusi”.
<p>Pengertian penyimpanan menurut Undang-undang Republik Indonesia Nomor 22 Tahun 2001 Tentang Minyak dan Gas Bumi pasal 1 angka 13 yaitu “Penyimpanan adalah kegiatan penerimaan, pengumpulan, penampungan, dan pengeluaran Minyak Bumi dan/atau Gas Bumi”.
<p>Pengertian Niaga menurut Undang-undang Republik Indonesia Nomor 22 Tahun 2001 Tentang Minyak dan Gas Bumi pasal 1 angka 14 yaitu “Niaga adalah kegiatan pembelian, penjualan, ekspor, impor Minyak Bumi dan/atau hasil olahannya, termasuk Niaga Gas Bumi melalui pipa”
<p>Setiap Badan Usaha dapat diberi lebih dari 1 (satu) Izin Usaha sepanjang tidak bertentangan dengan ketentuan peraturan perundang-undangan yang berlaku. Dalam hal Badan Usaha melakukan kegiatan usaha Pengolahan dengan kegiatan pengangkutan, penyimpanan dan/atau niaga sebagai kelanjutan kegiatan usaha Pengolahannya, maka kepada Badan Usaha hanya diwajibkan memiliki Izin Usaha Pengolahan.
<p>Dalam hal Badan Usaha sebagaimana dimaksud di atas melakukan kegiatan usaha niaga umum (wholesale) wajib mendapatkan Izin Usaha Niaga Umum terlebih dahulu. Badan Usaha pemegang Izin Usaha Niaga Umum (Wholesale) dalam menyalurkan Bahan Bakar Minyak untuk pengguna skala kecil, pelanggan kecil, transportasi dan rumah tangga wajib menyalurkannya melalui penyalur yang ditunjuk Badan Usaha melalui seleksi berdasarkan perjanjian kerjasama (wajib mengutamakan koperasi, usaha kecil dan/atau usaha swasta nasional).
<p>Penyalur sebagaimana dimaksud di atas hanya dapat menyalurkan BBM dengan merek dagang yang digunakan atau dimiliki Badan Usaha pemegang Izin Usaha Umum (Wholesale). Badan Usaha pemegang Izin Usaha Niaga Umum (Wholesale) dapat melakukan kegiatan penyaluran secara langsung kepada pengguna transportasi melalui fasilitas dan sarana yang dimiliki dan dikelolanya sendiri hanya sebatas paling banyak 20% dari jumlah seluruh sarana dan fasilitas penyaluran yang dikelola dan/atau dimilikinya.
<p>Menurut pasal 12 Peraturan Pemerintah Republik Indonesia Nomor 36 Tahun 2004 Tentang Kegiatan Usaha Hilir Minyak dan Gas Bumi, kegiatan usaha hilir, meliputi:
<br><br>a. kegiatan usaha Pengolahan yang meliputi kegiatan memurnikan, memperoleh bagian-bagian, mempertinggi mutu, dan mempertinggi nilai tambah Minyak dan Gas Bumi yang menghasilkan Bahan Bakar Minyak, Bahan Bakar Gas, Hasil Olahan, LPG dan/atau LNG tetapi tidak termasuk Pengolahan Lapangan;
<br><br>b. kegiatan usaha Pengangkutan yang meliputi kegiatan pemindahan Minyak Bumi, Gas Bumi, Bahan Bakar Minyak, Bahan Bakar Gas, dan/atau Hasil Olahan baik melalui darat, air, dan/atau udara termasuk Pengangkutan Gas Bumi Melalui Pipa dari suatu tempat ke tempat lain untuk tujuan komersial;
<br><br>c. kegiatan usaha Penyimpanan yang meliputi kegiatan penerimaan, pengumpulan, penampungan dan pengeluaran Minyak Bumi, Bahan Bakar Minyak, Bahan Bakar Gas, dan/atau Hasil Olahan pada lokasi di atas dan/atau di bawah permukaan tanah dan/atau permukaan air untuk tujuan komersial;
<br><br>d. kegiatan usaha Niaga yang meliputi kegiatan pembelian, penjualan, gekspor, impor Minyak Bumi, Bahan Bakar Minyak, Bahan Bakar Gas dan/atau Hasil Olahan, termasuk Gas Bumi melalui pipa.

